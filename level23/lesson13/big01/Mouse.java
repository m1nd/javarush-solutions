package com.javarush.test.level23.lesson13.big01;

public class Mouse
{
    private int x, y;

    public Mouse(int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX()
    {
        return x;
    }

    public int getY()
    {
        return y;
    }
}
